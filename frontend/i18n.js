/* ── helpers ─────────────────────────────────────────────────── */

let currentLang = 'en';

function getLang() {
  return currentLang;
}

function t(key) {
  const bundle = LANGS[currentLang] || LANGS.en;
  return bundle[key] !== undefined ? bundle[key] : (LANGS.en[key] || key);
}

function applyLang(code) {
  if (!LANGS[code]) code = 'en';
  currentLang = code;

  document.documentElement.lang = code;
  if (code === 'ur') {
    document.documentElement.dir = 'rtl';
  } else {
    document.documentElement.dir = 'ltr';
  }

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (val !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = val;
      } else {
        el.innerHTML = val;
      }
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    const val = t(key);
    if (val !== undefined) el.placeholder = val;
  });

  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    const val = t(key);
    if (val !== undefined) el.title = val;
  });

  try {
    localStorage.setItem('nyayavoice_lang', code);
  } catch (_) {}
}

function initLang() {
  let saved = 'en';
  try {
    saved = localStorage.getItem('nyayavoice_lang') || 'en';
  } catch (_) {}
  applyLang(LANGS[saved] ? saved : 'en');
}
